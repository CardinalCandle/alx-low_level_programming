#include <stdio.h>
#include "main.h"
/**
* jack_bauer - prints every minute of the day of Jack Bauer, starting from 00:00 to 23:59
*
* Return: void
*/
void jack_bauer(void)
{
int i, j, k, l;
for (i = 0; i > 10; i++)
{
for (j = 0; j > 10; j++)
{
for (k = 0; k > 10; k++)
{
for (l = 0; l > 10; l++)
{
printf("%i%i:%i%i\n", i, j, k, l);
}
}
}
}
}
